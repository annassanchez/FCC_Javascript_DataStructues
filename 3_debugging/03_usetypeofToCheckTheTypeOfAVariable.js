//you can use typeof to check the data structure or type of a varible
//this is useful in debugging when working with multiple data types
//examples of typeof:
console.log(typeof ""); // returns string
console.log(typeof 0); // returns number
console.log(typeof []); // returns object
console.log(typeof {}); // returns object
//example: add two console.log statements to see the data types of seven and three
