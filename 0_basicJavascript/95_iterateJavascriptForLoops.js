//for loops run an specific number of times
//for (a; b; c) where a is the initialization statement, b is the condition statement, c is the final expression
//the initialization statement executes once befor the loop starts -- it sets up the loop variable
//the condition statement is evaluated through every loop iteration
//the final expression is executed at the end of each loop iteration, prior to the next condition check
const ourArray = [];

for (let i = 0; i < 5; i++) {
  ourArray.push(i);
}
